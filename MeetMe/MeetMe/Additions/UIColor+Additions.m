#import "UIColor+Additions.h"

@implementation UIColor (Additions)

+ (UIColor*)rdvTertiaryColor
{
    return [UIColor colorWithRed:0.25 green:0.54 blue:0.88 alpha:1.0];
}

+ (UIColor*)rdvPrimaryColor
{
    return [UIColor colorWithRed:0.67 green:0.67 blue:0.67 alpha:1.0];
}

+ (UIColor*)rdvGreenColor
{
    return [UIColor colorWithRed:0.13 green:0.75 blue:0.39 alpha:1];
}

+ (UIColor*)rdvIgniterColor
{
    return [UIColor colorWithRed:0.71 green:0.27 blue:0.27 alpha:1.0];
}

@end
